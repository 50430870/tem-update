function runTimes() {
  return new Promise((resolve, reject) => {
    $.get({
      url: `https://wind/api/runTimes?activityId=farm&sharecode=${$.farmInfo.farmUserPro.shareCode}`
    }, (err, resp, data) => {
      if (err) {
        console.log('上报失败', err)
        reject(err)
      } else {
        console.log(data)
        resolve()
      }
    })
  })
}

